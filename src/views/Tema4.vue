      h1 Sistema Único de Acreditación en Salud (SUA)

    p El Sistema Único de Acreditación en Salud (SUA) es uno de los componentes del Sistema Obligatorio de Garantía de la Calidad (SOGC) y tiene como propósito promover el mejoramiento continuo de la calidad en la prestación de los servicios de salud. Su implementación se basa en un proceso voluntario mediante el cual las instituciones prestadoras de servicios de salud pueden demostrar el cumplimiento de estándares superiores de calidad, orientados a fortalecer la seguridad del paciente, la eficiencia institucional y la satisfacción de los usuarios.
    p A diferencia de otros mecanismos del Sistema Obligatorio de Garantía de la Calidad (SOGC), el Sistema Único de Acreditación en Salud (SUA) no se limita a verificar el cumplimiento de las condiciones mínimas exigidas para la prestación de los servicios. Su enfoque está dirigido a valorar el desempeño institucional mediante estándares que promueven la excelencia, la gestión por procesos, la mejora continua y la atención centrada en las personas.
